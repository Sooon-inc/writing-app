export function meoUserPrompt(hpContent: string, hearing: string): string {
  const parts = [];
  if (hpContent) parts.push(`【HPから取得した情報】\n${hpContent}`);
  if (hearing) parts.push(`【ヒアリング内容】\n${hearing}`);
  parts.push(
    "上記の情報をもとに、Googleビジネスプロフィール用のライティングを生成してください。"
  );
  return parts.join("\n\n");
}
