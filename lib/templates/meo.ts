export function meoUserPrompt(hpContent: string, hearing: string, products: string[], searchInfo?: string): string {
  const parts = [];
  if (hpContent) parts.push(`【HPから取得した情報】\n${hpContent}`);
  if (searchInfo) parts.push(`【Web検索結果（住所・最寄り駅・電話番号などの事実情報として優先使用）】\n${searchInfo}`);
  if (hearing) parts.push(`【ヒアリング内容】\n${hearing}`);
  if (products.length > 0) {
    const suggestion = products.length < 8
      ? `（現在${products.length}個。合計10個になるよう${10 - products.length}個を「商品サービス提案」に追加してください）`
      : `（8個以上のため「商品サービス提案」は空配列にしてください）`;
    parts.push(`【取り扱い商品・サービス】${suggestion}\n${products.map((p, i) => `${i + 1}. ${p}`).join("\n")}`);
  } else {
    parts.push("【取り扱い商品・サービス】（未入力。ビジネス内容から10個を「商品サービス提案」に提案してください）");
  }
  parts.push(
    "上記の情報をもとに、Googleビジネスプロフィール（MEO）の初期設計を行ってください。提供された情報にない内容はハルシネーションせず「情報なし」と記載してください。"
  );
  return parts.join("\n\n");
}
