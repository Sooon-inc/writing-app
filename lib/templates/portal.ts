export interface PortalInputData {
  hpContent: string;
  gbpContent: string;
  snsContent: string;
  hearing: string;
  mapIframe?: string;
}

export function portalUserPrompt(data: PortalInputData): string {
  const parts: string[] = [];

  if (data.hearing) parts.push(`【ヒアリング内容（最優先）】\n${data.hearing}`);
  if (data.hpContent) parts.push(`【会社ホームページ】\n${data.hpContent}`);
  if (data.gbpContent) parts.push(`【Googleビジネスプロフィール】\n${data.gbpContent}`);
  if (data.mapIframe) parts.push(`【Googleマップ埋め込みiframe（GoogleMap用住所に使用）】\n${data.mapIframe}`);
  if (data.snsContent) parts.push(`【SNS】\n${data.snsContent}`);
  if (parts.length === 0) parts.push("（提供情報なし）");

  parts.push(`上記をもとに、指定JSONの全キーを維持して出力してください。
- 施工事例は確認できたものだけを①〜③へ分ける
- 3つのお約束は、確認できる会社の方針・対応内容から作成する
- 代表インタビューの基本5問に回答する
- 追加質問は、掲載価値のある情報がある場合だけ作成する
- SNS欄には確認できたURLを入れる
- 「最寄りの駅」はGoogleビジネスプロフィール内の【最寄り駅】があれば必ずそのまま入れる
- 「GoogleMap用住所」はGoogleマップ埋め込みiframeがある場合、そのiframeコードだけを入れる
- 対応エリアは都道府県名を「、」で区切る
- タグは事業内容から該当するものだけtrueにする`);

  return parts.join("\n\n");
}

