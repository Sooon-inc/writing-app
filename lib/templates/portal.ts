export interface PortalInputData {
  hpContent: string;
  gbpContent: string;
  snsContent: string;
  hearing: string;
}

export function portalUserPrompt(data: PortalInputData): string {
  const parts: string[] = [];

  if (data.hearing) {
    parts.push(`【ヒアリング内容（最優先・最重要）】\n${data.hearing}`);
  }
  if (data.hpContent) {
    parts.push(`【ホームページから取得したテキスト（住所・電話・営業時間・施工事例・資格等を必ず抽出すること）】\n${data.hpContent}`);
  }
  if (data.gbpContent) {
    parts.push(`【Googleビジネスプロフィールから取得したテキスト（営業時間・住所・電話・サービス等を必ず抽出すること）】\n${data.gbpContent}`);
  }
  if (data.snsContent) {
    parts.push(`【SNSから取得したテキスト】\n${data.snsContent}`);
  }

  if (parts.length === 0) {
    parts.push("（提供情報なし）");
  }

  parts.push(
    `上記のすべての情報を徹底的に解析し、26項目すべてを埋めてください。
【重要な指示】
- ヒアリング内容を最優先で反映する
- ホームページ・GBPのテキストから住所・電話・営業時間・施工事例・対応地域・保有資格を必ず抽出する
- 施工事例はHP内の「施工事例」「実績」「works」等のセクションを特定して要約する
- 情報が見つからない項目のみ「記載なし」とする（安易に使わない）
- すべての26項目を必ず出力すること（項目抜け禁止）`
  );

  return parts.join("\n\n");
}

